public class Room {
    
    private String roomRoom;

    private String roomName;

    private String roomDescription;

    private String roomDirection1;

    private String roomDirection2;

    private String roomDirection3;

    private String roomStart;

    public Room(String roomRoom, String roomName, String roomDescription, String roomDirection1, String roomDirection2,
            String roomDirection3, String roomStart) {
        this.roomRoom = roomRoom;
        this.roomName = roomName;
        this.roomDescription = roomDescription;
        this.roomDirection1 = roomDirection1;
        this.roomDirection2 = roomDirection2;
        this.roomDirection3 = roomDirection3;
        this.roomStart = roomStart;
    }

    public String getRoomRoom() {
        return roomRoom;
    }

    public void setRoomRoom(String roomRoom) {
        this.roomRoom = roomRoom;
    }

    public String getRoomName() {
        return roomName;
    }

    public void setRoomName(String roomName) {
        this.roomName = roomName;
    }

    public String getRoomDescription() {
        return roomDescription;
    }

    public void setRoomDescription(String roomDescription) {
        this.roomDescription = roomDescription;
    }

    public String getRoomDirection1() {
        return roomDirection1;
    }

    public void setRoomDirection1(String roomDirection1) {
        this.roomDirection1 = roomDirection1;
    }

    public String getRoomDirection2() {
        return roomDirection2;
    }

    public void setRoomDirection2(String roomDirection2) {
        this.roomDirection2 = roomDirection2;
    }

    public String getRoomDirection3() {
        return roomDirection3;
    }

    public void setRoomDirection3(String roomDirection3) {
        this.roomDirection3 = roomDirection3;
    }

    public String getRoomStart() {
        return roomStart;
    }

    public void setRoomStart(String roomStart) {
        this.roomStart = roomStart;
    }

    
    

    

}
